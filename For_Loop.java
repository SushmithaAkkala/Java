package CFS_Loop_Statements;

public class For_Loop {

	public static void main(String[] args) {
		/*      looping is also known as iterations, control flow statements are
		 *      for, while, do while
			 */
				String a="Levitica";
				for(int i=0;i<5;i++)
				{
					System.out.println(a);
				}
				

			}
}
